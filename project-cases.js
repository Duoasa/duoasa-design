function createTextNode(tagName, text) {
  const node = document.createElement(tagName);
  node.textContent = text;
  return node;
}

function createMetaItem(label, value) {
  const wrapper = document.createElement("div");
  wrapper.append(createTextNode("dt", label), createTextNode("dd", value));
  return wrapper;
}

function createSection(section, index) {
  const article = document.createElement("article");
  article.className = "case-story-section";

  const indexNode = createTextNode("span", String(index + 1).padStart(2, "0"));
  indexNode.className = "case-story-index";

  const label = createTextNode("p", `${String(index + 1).padStart(2, "0")} - ${section.label}`);
  label.className = "section-label";

  const title = createTextNode("h2", section.title);
  const body = document.createElement("div");
  body.className = "case-story-copy";
  body.replaceChildren(...section.body.map((paragraph) => createTextNode("p", paragraph)));

  const content = document.createElement("div");
  content.className = "case-story-content";
  content.append(label, title, body);

  if (section.bullets && section.bullets.length) {
    const list = document.createElement("ul");
    list.className = "case-list";
    list.replaceChildren(...section.bullets.map((item) => createTextNode("li", item)));
    content.append(list);
  }

  article.append(indexNode, content);
  return article;
}

function renderCase() {
  const params = new URLSearchParams(window.location.search);
  const id = params.get("id") || projectCases[0].id;
  const currentIndex = projectCases.findIndex((item) => item.id === id);
  const project = projectCases[currentIndex] || projectCases[0];
  const nextProject = projectCases[(projectCases.indexOf(project) + 1) % projectCases.length];

  document.title = `${project.title} - Duoasa Case`;

  const caseNumber = document.getElementById("caseNumber");
  const caseTitle = document.getElementById("caseTitle");
  const caseYear = document.getElementById("caseYear");
  const caseStatus = document.getElementById("caseStatus");
  const caseSummary = document.getElementById("caseSummary");
  const caseMeta = document.getElementById("caseMeta");
  const caseTags = document.getElementById("caseTags");
  const caseVisual = document.getElementById("caseVisual");
  const caseImage = document.getElementById("caseImage");
  const caseSections = document.getElementById("caseSections");
  const caseMetrics = document.getElementById("caseMetrics");
  const caseNext = document.getElementById("caseNext");

  caseNumber.textContent = project.number;
  caseTitle.textContent = project.title;
  caseYear.textContent = project.year;
  caseStatus.className = `project-status ${project.statusClass}`;
  caseStatus.innerHTML = "<span></span>";
  caseStatus.append(project.status);
  caseSummary.textContent = project.summary;

  caseVisual.className = `project-visual case-visual ${project.visualClass}`;
  caseImage.src = project.image;
  caseImage.alt = project.imageAlt;

  caseMeta.replaceChildren(
    createMetaItem("Company", project.company),
    createMetaItem("Year", project.year),
    createMetaItem("Type", project.type),
    createMetaItem("Role", project.role)
  );

  caseTags.replaceChildren(...project.tags.map((tag) => createTextNode("span", tag)));
  caseSections.replaceChildren(...project.sections.map((section, index) => createSection(section, index)));
  caseMetrics.replaceChildren(...project.metrics.map((metric) => {
    const wrapper = document.createElement("div");
    wrapper.append(createTextNode("strong", metric.value), createTextNode("span", metric.label));
    return wrapper;
  }));

  caseNext.href = nextProject.href || "index.html#project";
  caseNext.querySelector("strong").textContent = nextProject.title;
}

renderCase();
